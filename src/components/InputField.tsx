import React from "react";
import CardLabel from "./label/CardLabel";

type InputFieldProps = {
  label: string;
  value: string;
  onChange: (e: React.ChangeEvent<HTMLInputElement | HTMLTextAreaElement>) => void;
  readOnly: boolean;
  isSingleLine?: boolean;
  id: string;
};

export default function InputField({ label, value, onChange, readOnly, isSingleLine = true, id }: InputFieldProps) {
  return (
    <div className="mb-4">
      <CardLabel html={id} text={label} hasBorder={false} hasPadding={false} />
      {isSingleLine ? (
        <input
          id={id}
          value={value}
          onChange={onChange}
          readOnly={readOnly}
          className={`w-full outline-none transition-all duration-2000  ${
            readOnly ? "" : "px-2 py-1 border border-stone-300"
          }`}
        />
      ) : (
        <textarea
          id={id}
          value={value}
          onChange={onChange}
          readOnly={readOnly}
          className={`w-full outline-none transition-all duration-2000 ${
            readOnly ? "" : " px-2 py-1 border  border-stone-300"
          }`}
        />
      )}
    </div>
  );
}
